static uint8_t fb[1024]; /* left[512] + right[512] */

static void fb_pixel(int x, int y, int on) {
    if (x < 0 || x >= 128 || y < 0 || y >= 64) return;
    int panel = x / 64;
    int col = x % 64;
    int page = y / 8;
    int bit = y % 8;
    int idx = panel * 512 + page * 64 + col;
    if (on) fb[idx] |= (1 << bit);
    else    fb[idx] &= ~(1 << bit);
}

static void fb_text(int x, int y, const char *s) {
    for (; *s; s++) {
        int ch = *s;
        if (ch < ' ' || ch > 'z') ch = ' ';
        const uint8_t *g = font[ch - ' '];
        for (int c = 0; c < 5; c++)
            for (int r = 0; r < 8; r++)
                if (g[c] & (1 << r))
                    fb_pixel(x + c, y + r, 1);
        x += 6;
    }
}

int main(void) {
    int sfd = open("/dev/cuau1", O_RDWR | O_NOCTTY);
    struct termios tio;
    tcgetattr(sfd, &tio); cfmakeraw(&tio);
    cfsetispeed(&tio, B115200); cfsetospeed(&tio, B115200);
    tio.c_cflag |= (CLOCAL | CREAD | CS8);
    tio.c_cflag &= ~(PARENB | CSTOPB | CRTSCTS);
    tio.c_cc[VMIN]=0; tio.c_cc[VTIME]=1;
    tcsetattr(sfd, TCSANOW, &tio);
    tcflush(sfd, TCIOFLUSH);

    /* Render inverted: white background, dark text */
    memset(fb, 0x00, 1024);  /* all pixels ON = white */
    fb_text(4,  4,  "CHECK POINT P-210");
    fb_text(4,  16, "OPNsense 23.1.6");
    fb_text(4,  28, "INVERTED DISPLAY");
    fb_text(4,  40, "Dark on Light!");
    fb_text(4,  52, "128x64 Graphics");

    /* XOR to match display polarity (0=pixel on) */
    for (int i = 0; i < 1024; i++)
        fb[i] ^= 0xFF;

    /* Send: ESC @, ESC G, then interleaved L/R pages */
    uint8_t init[] = {0x1B, 0x40};
    write(sfd, init, 2); tcdrain(sfd); usleep(100000);
    uint8_t gfx[] = {0x1B, 0x47};
    write(sfd, gfx, 2); tcdrain(sfd); usleep(50000);

    for (int p = 0; p < 8; p++) {
        write(sfd, &fb[p * 64], 64);           /* left panel page p */
        tcdrain(sfd); usleep(2000);
        write(sfd, &fb[512 + p * 64], 64);     /* right panel page p */
        tcdrain(sfd); usleep(2000);
    }

    int sp = open("/dev/speaker", O_WRONLY);
    if (sp >= 0) { write(sp, "O2L8C", 5); close(sp); }
    printf("Inverted graphics sent\n");
    close(sfd);
    return 0;
}
